package com.wenbronk.spring03.aop.xml.domain;

/**
 * @Author wenbronk
 * @Date 2019-06-27
 */
public class Account {

    private String id;
    private String name;
    private float money;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getMoney() {
        return money;
    }

    public void setMoney(float money) {
        this.money = money;
    }
}
